//Print Pattern :
//   * * * * 
//   * * *
//   * *
//   *
#include <bits/stdc++.h>
using namespace std;
int main() {
        for(int j=0;j<4;j++){
            for(int i=j;i<4;i++)
            {
             cout<<"*";
             cout<<" ";
            }
            cout<<"\n";
        }
    return 0;
}